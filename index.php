                <h2>Why shooting for the Clouds?</h2>
                <div class="image_frame float_l"><span></span><img src="images/tooplate_image_01.jpg" alt="image 1" /></div>
                <div class="image_frame float_r"><span></span><img src="images/tooplate_image_02.png" alt="image 2" /></div>
                <div class="cleaner"></div>
                <p>Cloud computing is a general term for the delivery of hosted services over the internet.</p>
                <p>Cloud computing enables companies to consume a compute resource, such as a virtual machine (VMs), storage or an application, as a utility -- just like electricity -- rather than having to build and maintain computing infrastructures in house.</p>
