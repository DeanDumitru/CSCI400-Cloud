            <div class="cbox_fw">
            	<h2>Main benefits of C&D Cloud Computing Services</h2>
                <div class="image_frame image_fl"><span></span><img src="images/tooplate_image_05.jpeg" alt="image 5" /></div>
                <a href="#">Find more about the Cloud here</a>
                <p><b>Self-service provisioning:</b> End users can spin up compute resources for almost any type of workload on demand. This eliminates the traditional need for IT administrators to provision and manage compute resources.</p>
				<p><b>Elasticity:</b> Companies can scale up as computing needs increase and scale down again as demands decrease. This eliminates the need for massive investments in local infrastructure which may or may not remain active.</p>
				<p><b>Pay per use:</b> Compute resources are measured at a granular level, allowing users to pay only for the resources and workloads they use.</p>
